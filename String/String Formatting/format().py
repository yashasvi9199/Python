### Simple Declaration
print("We are all {}".format("equal"))

### Index Base
print("{0} {1} {2}".format("Read","the","directions"))

### Assigning Values
print("a:{a}, b:{b}, c:{c}".format(a=1, b=2, c=3))

### Re-use Assigned Values
print("The first {p} was alright, but the {p} {p} was tough".format(p="second"))



