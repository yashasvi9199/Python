## LISTS
li = ["Welcome", "to", "learning", "Python"]
for i in li:
    print(i)

## Tuples
tup = ("Welcome", "to", "learning", "Python")
for i in tup:
    print(i)

## Set
sett = {1,2,3,4,5,6,7,8,9,0}
for i in sett:
    print(i)

## String
str = "Welcome to learning Python"
for i in str:
    print(i)


## Dictionary
dic = dict( {'a':100, 'b':200} )
for i in dic:
    print("%s %d" % (i, dic[i]))       #check % operator.txt file in Basics